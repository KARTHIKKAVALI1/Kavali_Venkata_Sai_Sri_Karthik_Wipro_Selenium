package day1_and_day2;

//3.	Write a Java program that demonstrates the use of all primitive data types.

public class primitiveDataTypes {
    public static void main(String[] args) {
        byte byteVal = 100;
        short shortVal = 10000;
        int intVal = 100000;
        long longVal = 10000000000L;

        float floatVal = 10.5f;
        double doubleVal = 20.99;

        char charVal = 'A';
        boolean boolVal = true;

        System.out.println("Value Of byte: " + byteVal);
        System.out.println("Value Of short: " + shortVal);
        System.out.println("Value Of int: " + intVal);
        System.out.println("Value Of long: " + longVal);
        System.out.println("Value Of float: " + floatVal);
        System.out.println("Value Of double: " + doubleVal);
        System.out.println("Value Of char: " + charVal);
        System.out.println("Value Of boolean: " + boolVal);
    }
}
